import React, { useEffect, useRef } from 'react';
import Chart, { ChartConfiguration } from 'chart.js/auto';

interface DailyDataPoint {
  date: string;
  delayed: number;
  onTime: number;
  notUploaded: number;
}

interface TATLineChartProps {
  data: DailyDataPoint[];
}

const TATLineChart: React.FC<TATLineChartProps> = ({ data = [] }) => {
  const chartRef = useRef<HTMLCanvasElement>(null);
  const chartInstance = useRef<Chart | null>(null);

  useEffect(() => {
    if (!chartRef.current || !data || data.length === 0) return;

    if (chartInstance.current) {
      chartInstance.current.destroy();
    }

    const ctx = chartRef.current.getContext('2d');
    if (!ctx) return;

    const labels = data.map(d => {
      const date = new Date(d.date);
      return date.toLocaleDateString('en-US', { month: 'short', day: 'numeric' });
    });
    const delayedData = data.map(d => d.delayed || 0);
    const onTimeData = data.map(d => d.onTime || 0);
    const notUploadedData = data.map(d => d.notUploaded || 0);

    const config: ChartConfiguration = {
      type: 'line',
      data: {
        labels,
        datasets: [
          {
            label: 'Delayed',
            data: delayedData,
            borderColor: '#f44336',
            backgroundColor: 'rgba(244, 67, 54, 0.1)',
            fill: true,
            tension: 0.4,
            borderWidth: 2,
            pointRadius: 3,
            pointBackgroundColor: '#f44336',
            pointBorderColor: '#fff',
            pointBorderWidth: 1,
          },
          {
            label: 'On Time',
            data: onTimeData,
            borderColor: '#4caf50',
            backgroundColor: 'rgba(76, 175, 80, 0.1)',
            fill: true,
            tension: 0.4,
            borderWidth: 2,
            pointRadius: 3,
            pointBackgroundColor: '#4caf50',
            pointBorderColor: '#fff',
            pointBorderWidth: 1,
          },
          {
            label: 'Not Uploaded',
            data: notUploadedData,
            borderColor: '#9E9E9E',
            backgroundColor: 'rgba(158, 158, 158, 0.1)',
            fill: true,
            tension: 0.4,
            borderWidth: 2,
            pointRadius: 3,
            pointBackgroundColor: '#9E9E9E',
            pointBorderColor: '#fff',
            pointBorderWidth: 1,
          },
        ],
      },
      options: {
        responsive: true,
        maintainAspectRatio: false,
        plugins: {
          legend: { 
            display: true, 
            position: 'bottom',
            labels: {
              usePointStyle: true,
              padding: 20
            }
          },
          tooltip: {
            mode: 'index',
            intersect: false
          }
        },
        scales: {
          x: {
            title: { display: true, text: 'Date', color: '#666' },
            grid: { display: false },
            ticks: { color: '#666' }
          },
          y: {
            beginAtZero: true,
            title: { display: true, text: 'Number of Requests', color: '#666' },
            grid: { color: 'rgba(0,0,0,0.05)' },
            ticks: { color: '#666' }
          },
        },
      },
    };

    chartInstance.current = new Chart(ctx, config);

    return () => {
      if (chartInstance.current) {
        chartInstance.current.destroy();
      }
    };
  }, [data]);

  return (
    <div style={{ width: '100%', height: '100%', minHeight: '350px' }}>
      <canvas ref={chartRef} />
    </div>
  );
};

export default TATLineChart;